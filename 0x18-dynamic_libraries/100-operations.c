#include"ops.h"
/**
  * add - adds two ints together
  * @a: first int
  * @b: second int
  * Return: added value
  */
int add(int a, int b)
{
	return (a + b);
}
/**
  * sub - subtracts two ints
  * @a: first int
  * @b: second int
  * Return: subbed value
  */
int sub(int a, int b)
{
	return (a - b);
}
/**
  * mul - multiplies two ints together
  * @a: first int
  * @b: second int
  * Return: multiplied value
  */
int mul(int a, int b)
{
	return (a * b);
}
/**
  * div - divides two ints together
  * @a: first int
  * @b: second int
  * Return: divided value
  */
int div(int a, int b)
{
	return (a / b);
}
/**
  * mod - remainder of two ints
  * @a: first int
  * @b: second int
  * Return: remainder value
  */
int mod(int a, int b)
{
	return (a % b);
}
